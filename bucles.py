# Loop while
age = 0
# Increment the age until it reaches 18
while age < 18:
    age += 1
    print("Congrats, you are", age, "years old")

#Loop for in a string
string = input("Enter a string: ")
for letter in string:
    print(letter)

# Loop for in a range
for number in range(1, 101):
    print(number)